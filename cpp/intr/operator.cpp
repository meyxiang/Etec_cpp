#include<iostream>

using namespace std;

int main()
{   
    // note true = 1 or flase = 0
    cout<<"10 < 10 = "<<(10<10)<<endl;
    cout<<"10 > 10 = "<<(10>10)<<endl;
    cout<<"10 <= 10 = "<<(10<=10)<<endl;
    cout<<"10 >= 10 = "<<(10>=10)<<endl;
    cout<<"10 != 10 = "<<(10!=10)<<endl;
    cout<<"10 == 10 = "<<(10==10)<<endl;

    cout<<"(10 == 10) && (10 != 10) = "<<((10==10)&&(10!=10))<<endl;
    cout<<"(10 <= 10) && (10 >= 10) = "<<((10<=10)&&(10>=10))<<endl; 
    cout<<"(10 > 10) && (10 < 10) = "<<((10>10)&&(10<10))<<endl;

    cout<<"(10 == 10) || (10 != 10) = "<<((10==10)||(10!=10))<<endl;
    cout<<"(10 <= 10) || (10 >= 10) = "<<((10<=10)||(10>=10))<<endl; 
    cout<<"(10 > 10) || (10 < 10) = "<<((10>10)||(10<10))<<endl;
    
    return 0;
}